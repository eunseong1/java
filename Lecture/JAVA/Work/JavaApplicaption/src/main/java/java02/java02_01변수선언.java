package java02;

public class java02_01변수선언 {
	
		public static void main(String[] args) {
		
			boolean e = true;
			char    c = 'a';
			int    i = 0;
			double d = 0.05;
			
			System.out.println(e);
			System.out.println(c);
			System.out.println(i);
			System.out.println(d);
			
			e = false;
			c = 'A';
			i = 100;
			d = 20.17;
			
			System.out.println(e);
			System.out.println(c);
			System.out.println(i);
			System.out.println(d);
			
	
				
		}
}
